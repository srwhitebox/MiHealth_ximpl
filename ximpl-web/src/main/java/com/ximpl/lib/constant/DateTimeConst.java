package com.ximpl.lib.constant;

public class DateTimeConst {
	public static final String FORMAT_DATE_4_DIGIT_PASSWORD = "MMdd";
	public static final String FORMAT_DATE_6_DIGIT_PASSWORD = "yyMMdd";
	public static final String FORMAT_DATE_8_DIGIT_PASSWORD = "yyyyMMdd";
	
	public static final long ONE_MINUTES = 60 * 1000;
	public static final long ONE_HOUR = ONE_MINUTES * 60;
	public static final long HALF_HOUR = ONE_MINUTES * 30;
	public static final long QUATER_HOUR = ONE_MINUTES * 15;

	public static final long ONE_DAY = ONE_HOUR * 24;
	public static final long ONE_WEEK = ONE_DAY * 7;
}
